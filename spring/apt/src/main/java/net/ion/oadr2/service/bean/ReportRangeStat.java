package net.ion.oadr2.service.bean;

public class ReportRangeStat {
	long from;
	long to;
	long count;
	double max;
	double mean;
	double min;
	double total;
	long duration;
	float value;
	long baselineValue;
	long peakRateValue;
	double baselinePrice;
	double peakRatePrice;
	double realPrice;
	boolean optIn;
	
	public ReportRangeStat() {
	}
	
	public ReportRangeStat(long from, long to) {
		this.from = from;
		this.to = to;
	}
	
	public ReportRangeStat(long from, long to, long duration) {
		this.from = from;
		this.to = to;
		this.duration = duration;
	}
	
	public long getFrom() {
		return from;
	}
	public void setFrom(long from) {
		this.from = from;
	}
	public long getTo() {
		return to;
	}
	public void setTo(long to) {
		this.to = to;
	}
	public float getValue() {
		return value;
	}
	public void setValue(float value) {
		this.value = value;
	}
	public long getBaselineValue() {
		return baselineValue;
	}
	public void setBaselineValue(long baselineValue) {
		this.baselineValue = baselineValue;
	}
	public long getPeakRateValue() {
		return peakRateValue;
	}
	public void setPeakRateValue(long peakRateValue) {
		this.peakRateValue = peakRateValue;
	}
	public long getDuration() {
		return duration;
	}
	public void setDuration(long duration) {
		this.duration = duration;
	}
	public double getBaselinePrice() {
		return Math.round(baselinePrice);
	}
	public void setBaselinePrice(double baselinePrice) {
		this.baselinePrice = baselinePrice;
	}
	public double getPeakRatePrice() {
		return Math.round(peakRatePrice);
	}
	public void setPeakRatePrice(double peakRatePrice) {
		this.peakRatePrice = peakRatePrice;
	}
	public long getCount() {
		return count;
	}
	public void setCount(long count) {
		this.count = count;
	}
	public double getRealPrice() {
		return Math.round(realPrice);
	}
	public void setRealPrice(double realPrice) {
		this.realPrice = realPrice;
	}
	public boolean isOptIn() {
		return optIn;
	}
	public void setOptIn(boolean optIn) {
		this.optIn = optIn;
	}
	public double getMax() {
		return max;
	}
	public void setMax(double max) {
		this.max = max;
	}
	public double getMean() {
		return mean;
	}
	public void setMean(double mean) {
		this.mean = mean;
	}
	public double getMin() {
		return min;
	}
	public void setMin(double min) {
		this.min = min;
	}
	public double getTotal() {
		return total;
	}
	public void setTotal(double total) {
		this.total = total;
	}
	
}
